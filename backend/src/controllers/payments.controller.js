import { supabaseAdmin } from '../lib/supabaseClient.js';
import { serializeOrder } from '../lib/serialize.js';
import { ApiError } from '../lib/ApiError.js';

// ============================================================================
// Demo GCash payment module.
//
// This is a SIMULATION for a capstone demo — there is no real GCash Merchant API call
// anywhere in this file. Everything here is scoped to what already exists on `orders`
// (payment_method/payment_status/transaction_id/paid_at — see supabase/schema.sql) so
// swapping this out for the real GCash API later only means replacing the body of
// confirmGcashPayment with a real webhook/callback handler; the routes, the request shape,
// and the rest of the app never need to change.
// ============================================================================

const MERCHANT_NAME = 'HarvestLink';

// GCash's own reference numbers read as four groups of four digits — this is purely
// cosmetic (shown before payment, never persisted) so the demo page looks authentic.
function generateReferenceNumber() {
  const group = () => String(Math.floor(1000 + Math.random() * 9000));
  return `${group()} ${group()} ${group()} ${group()}`;
}

// The real, persisted identifier for a completed demo payment — generated fresh only once
// the simulated payment actually "completes" (see confirmGcashPayment below).
function generateTransactionId() {
  const random = Math.random().toString(36).slice(2, 8).toUpperCase();
  return `GC${Date.now().toString(36).toUpperCase()}${random}`;
}

async function fetchOrderOr404(orderId) {
  const { data, error } = await supabaseAdmin.from('orders').select('*').eq('id', orderId).single();
  if (error || !data) throw new ApiError('Order was not found.', 404);
  return data;
}

function assertBuyer(req, order) {
  if (req.profile.id !== order.buyer_id) throw new ApiError('You do not have permission to pay for this order.', 403);
}

// GET /api/payments/gcash/:orderId — order + display-only checkout details for the demo
// GCash payment page (merchant name, amount, a freshly generated reference number). Nothing
// here is written to the database — the reference number is cosmetic, shown only until the
// buyer actually pays; the real, persisted transaction_id is generated by confirmGcashPayment.
export async function getGcashCheckout(req, res) {
  const order = await fetchOrderOr404(req.params.orderId);
  assertBuyer(req, order);
  if (order.payment_method !== 'gcash') throw new ApiError('This order is not a GCash payment.', 400);
  if (order.payment_status === 'paid') throw new ApiError('This order has already been paid.', 400);

  res.json({
    order: serializeOrder(order),
    merchantName: MERCHANT_NAME,
    referenceNumber: generateReferenceNumber(),
  });
}

// POST /api/payments/gcash/:orderId/confirm — called by the frontend once its simulated
// "processing" animation finishes (see src/features/payments/GcashPaymentPage.jsx). This is
// the one place a real integration would plug in: a genuine GCash callback/webhook would hit
// this exact route instead of the client calling it directly, with everything else
// (frontend flow, order fields, response shape) staying identical.
export async function confirmGcashPayment(req, res) {
  const order = await fetchOrderOr404(req.params.orderId);
  assertBuyer(req, order);
  if (order.payment_method !== 'gcash') throw new ApiError('This order is not a GCash payment.', 400);

  // Idempotent — a retried request (e.g. the buyer double-clicked, or a flaky connection
  // resent it) returns the already-paid order instead of erroring or generating a second
  // transaction_id for the same payment.
  if (order.payment_status === 'paid') {
    return res.json(serializeOrder(order));
  }

  const { data: updated, error } = await supabaseAdmin
    .from('orders')
    .update({
      payment_method: 'gcash',
      payment_status: 'paid',
      transaction_id: generateTransactionId(),
      paid_at: new Date().toISOString(),
    })
    .eq('id', order.id)
    .select()
    .single();
  if (error) throw new ApiError(error.message, 400);
  res.json(serializeOrder(updated));
}
